/*
 * some usefull functions for work with database above
 */
static int __cdecl
find_GetRuntimeClass_struct(const void *key, const void *elem)
{
  int ord = *(int *)key;
  struct GetRuntimeClass_struct *s = (GetRuntimeClass_struct *)elem;
  if ( ord < s->ord )
   return -1;
  if ( ord > s->ord )
   return 1;
  return 0;
}

static const char *find_GetRuntimeClass_struct_by_ord
  (int ord, const struct GetRuntimeClass_struct *s, int s_len)
{
  struct GetRuntimeClass_struct *res = (struct GetRuntimeClass_struct *)
   bsearch((const void *)ord, (const void *)s,
    s_len, sizeof(struct GetRuntimeClass_struct),
    find_GetRuntimeClass_struct
   );
  if ( res )
   return res->class_name;
  return 0;
}

char *get_cruntime_const(char *name)
{
  const char *CRC_suffix = "CRuntimeClass@@B";
  const int CRC_suffix_len = 16;
  const char *prefix = "?class";
  const int prefix_len = 6;
  char *ono = strstr(name, CRC_suffix);
  if ( !ono )
   return NULL;
  if ( CRC_suffix_len != strlen(ono) )
   return NULL;
  if ( strncmp(name, prefix, prefix_len) )
   return NULL;
  name = name + prefix_len;
  ono = name;
  while( *ono )
  {
    if (*ono == '@')
     break;
    ono++;
  }
  if ( !*ono )
   return NULL;
  int len = ono - name;
  ono = (char *)qalloc(len + 1);
  strncpy(ono, name, len);
  ono[len] = 0;
  return ono;
}

